    public void ajouterDepartementAuCombobox() {
        jComboBox1.removeAllItems();
        Departement depTemp;
        for (int i = 0; i < musee.listeDepartement.size(); i++) {
            depTemp = (Departement) musee.listeDepartement.elementAt(i);
            jComboBox1.addItem(depTemp.getNomDepartement());
        }
    }
    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        String dep = JOptionPane.showInputDialog("Veuillez saisir nom departement");

        musee.listeDepartement.add(new Departement(musee.listeDepartement.size() + 1, dep));
        visite.ajouterDepartementAuCombobox();
        ajouterDepartementAuCombobox();
    }//GEN-LAST:event_jMenuItem2ActionPerformed

