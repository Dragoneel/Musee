    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int i = jTable1.getSelectedRow();
        Vector v = Visite.recherche(Integer.parseInt(jTable1.getValueAt(i, 0).toString()), dep.listeOeuvre);
        Oeuvre oeuvre = (Oeuvre) v.elementAt(1);
        int indice = Integer.parseInt(v.elementAt(0).toString());
//        dep.deleteOeuvre(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));
        dep.listeOeuvre.set(indice, new Oeuvre(oeuvre.getIdOeuvre(), jTextField1.getText(), oeuvre.getAdresseImage(), jTextArea1.getText(), dep.getIdDep(jComboBox1.getSelectedItem().toString(), musee.listeDepartement)));
        JOptionPane.showMessageDialog(null, "Oeuvre bien modifiÃ©!");
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

