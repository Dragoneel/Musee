    public void afficheOeuvre(Oeuvre oeuvre) {

        jTextField1.setText(oeuvre.getNom());
        jTextArea1.setText(oeuvre.getDescription());

    }

    public void genererTableau(Vector v) {
        int taille = v.size();
        Oeuvre temp;
        jTable1.setModel(new DefaultTableModel(entete, taille) {
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
        jTable1.setToolTipText("Cliquer pour modifier");
        for (int i = 0; i < taille; i++) {

            temp = (Oeuvre) v.elementAt(i);
            jTable1.setValueAt(temp.getIdOeuvre(), i, 0);
            jTable1.setValueAt(temp.getNom(), i, 1);
            jTable1.setValueAt(temp.getDescription(), i, 2);
        }
    }

  

